from flask import Flask, render_template, redirect, url_for
import solver
app = Flask(__name__)

board = [[5, 3, 0, 0, 7, 0, 0, 0, 0],
         [6, 0, 0, 1, 9, 5, 0, 0, 0],
         [0, 9, 8, 0, 0, 0, 0, 6, 0],
         [8, 0, 0, 0, 6, 0, 0, 0, 3],
         [4, 0, 0, 8, 0, 3, 0, 0, 1],
         [7, 0, 0, 0, 2, 0, 0, 0, 6],
         [0, 6, 0, 0, 0, 0, 2, 8, 0],
         [0, 0, 0, 4, 1, 9, 0, 0, 5],
         [0, 0, 0, 0, 8, 0, 0, 7, 9]]


@app.route('/')
def hello_world():
    return render_template('index.html', board=board)


@app.route('/solve')
def solve():
    global board
    board = solver.solveBoard(board)
    return redirect(url_for('hello_world'))
